#include "../../include/minishell.h"

int	ret_null(char c)
{
	return ((c >= '1' && c <= '9') || c == '*' || c == '@' || c == '!'
		|| c == '(' || c == '[' || c == 92 || c == '{' || c == ')' || c == ';'
		|| c == '#' || c == '-');
}

int	put_directly(char c, int *i)
{
	if (c == '%' || c == '+' || c == ',' || c == '.' || c == '/' || c == ':'
		|| c == '=' || c == ']' || c == '^' || c == '}' || c == '~' || c == ' ')
	{
		*i += 1;
		return (1);
	}
	if (c == '\0' || is_quotes(c))
		return (1);
	return (0);
}

int	special_chars(char c)
{
	return (c == '0' || c == '?');
}
